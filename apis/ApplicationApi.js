const cleanHeaders = headers =>
  Object.fromEntries(Object.entries(headers).filter(kv => kv[1] != null));

export const createShoutoutPOST = async (
  Constants,
  _args,
  handlers,
  timeout
) => {
  const url = `https://x5dq-3lan-lmlo.n7d.xano.io/api:6F6O_jUf/`;
  const controller = new AbortController();
  let timeoutObj;
  if (timeout) {
    timeoutObj = setTimeout(() => {
      const err = new Error(`Timeout after ${timeout}ms`);
      err.__type = 'TIMEOUT';
      controller.abort(err);
    }, timeout);
  }
  try {
    const res = await fetch(url, {
      body: JSON.stringify({ key: 'value' }),
      headers: cleanHeaders({
        Accept: 'application/json',
        Authorization: Constants['AUTHORIZATION_TOKEN'],
        'Content-Type': 'application/json',
        'X-Branch': Constants['ENVIRONMENT'],
        'X-Data-Source': Constants['DATASOURCE_ENV'],
      }),
      method: 'POST',
      signal: controller.signal,
    });
    timeoutObj && clearTimeout(timeoutObj);
    return handleResponse(res, handlers);
  } catch (e) {
    if (e.__type === 'TIMEOUT') {
      handlers.onTimeout?.();
    } else if (timeoutObj) {
      clearTimeout(timeoutObj);
    }
    throw e;
  }
};

